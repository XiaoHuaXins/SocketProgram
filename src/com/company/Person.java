package com.company;

import java.io.Serializable;

/**
 * @Author xhx
 * @Date 2020/7/24 17:23
 */
public class Person implements Serializable {
    private final long serialVersionUID = 425675675635L;
    private String name;
    private int age;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    @Override
    public String toString() {
        return "Person{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }


}
